"""
这是介绍字符串功能函数
2024-3-5
"""
a = "HelloWord"
print(a[2])  # 字符串索引
print(a[1:3])  # 字符串切片，左包含右不包含
print(a.title())  # 各个单词首字母大写
print(a.upper())  # 所有字母大写
print(a.lower())  # 所有字母小写
"""
这是介绍列表功能函数
2024-3-6
"""
b=[1,2,3,4,5]
print(b[2])  # 列表索引
print(b[1:3])  # 列表切片，左包含右不包含
print(index(2)) # 获取指定元素第一次出现的位置
print(len(b)) # 获取列表的长度